public class Cycle {
	
	public static void main(String[] args) {

	for(int i = 0; i <= 20; i++) {
	   System.out.println(i); 
	 }

	 int a = -6;
	 while(a < 7) {
	 	System.out.println(a);
	 	a++;
	 }

	 int p = 9;
        do {
            p = p + 2;
            System.out.println(p);
        } while(p < 18);
    }
}